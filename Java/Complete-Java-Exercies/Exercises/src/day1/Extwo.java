package day1;

public class Extwo {
	boolean a;
	byte b; 
	char c;
	short d;
	int e;
	long f;
	float g;
	double h;
	public static void main(String[] args) {
		Extwo two = new Extwo();
		System.out.println("\nBoolean: "+ two.a + "\nByte: " + two.b + "\nChar: " + two.c + "\nShort: " + two.d + "\nInt: " + two.e + "\nLong: " + two.f + "\nFloat: " + two.g + "\nDouble: " + two.h);
	}
}
